public class Contato {

    String nome;
    String email;
    String telefone;
    String dataNascimento;

    public Contato(String nome, String email, String telefone, String dataNascimento) {
        this.nome = nome;
        this.email = email;
        this.telefone = telefone;
        this.dataNascimento = dataNascimento;
    }


}
